-- 1. Create SUPPLIERS Table
CREATE TABLE SUPPLIERS (
    SupplierID NUMBER PRIMARY KEY,
    SupplierName VARCHAR2(100) NOT NULL,
    Region VARCHAR2(50) NOT NULL,
    ParentSupplierID NUMBER,
    CONSTRAINT fk_parent_supplier FOREIGN KEY (ParentSupplierID) REFERENCES SUPPLIERS(SupplierID)
);

-- 2. Create COFFEE_BEANS Table
CREATE TABLE COFFEE_BEANS (
    BeanID NUMBER PRIMARY KEY,
    BeanName VARCHAR2(100) NOT NULL,
    Origin VARCHAR2(50) NOT NULL,
    SupplierID NUMBER NOT NULL,
    PricePerKg NUMBER(10,2) NOT NULL,
    StockKg NUMBER NOT NULL,
    CONSTRAINT fk_bean_supplier FOREIGN KEY (SupplierID) REFERENCES SUPPLIERS(SupplierID)
);

-- 3. Create ORDERS Table
CREATE TABLE ORDERS (
    OrderID NUMBER PRIMARY KEY,
    BeanID NUMBER NOT NULL,
    OrderDate DATE NOT NULL,
    QuantityKg NUMBER NOT NULL,
    TotalAmount NUMBER(10,2) NOT NULL,
    CONSTRAINT fk_order_bean FOREIGN KEY (BeanID) REFERENCES COFFEE_BEANS(BeanID)
);

